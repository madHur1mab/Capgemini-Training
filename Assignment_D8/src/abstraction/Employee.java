package abstraction;

public class Employee {
	int id;
	String name;
	double sal;
	String department;
	int experience;

	public Employee(int id, String name, double sal, String department, int experience) {
		this.id = id;
		this.name = name;
		this.sal = sal;
		this.department = department;
		this.experience = experience;
	}
}
