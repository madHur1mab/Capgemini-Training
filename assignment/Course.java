package assignment;

public class Course {
	private int courseid;
	private String courseName;
	
	public int getId() {
		return courseid;
	}
	public void setId(int courseid) {
		this.courseid = courseid;
	}
	public String getCourse() {
		return courseName;
	}
	public void setCourse(String courseName) {
		this.courseName = courseName;
	}
}
